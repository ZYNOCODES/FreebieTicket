package com.example.freebieticket.User;

public class DiscoverModel {
    private int iconCardDiscover;
    private String titleCardDiscover;

    public DiscoverModel(int iconCardDiscover, String titleCardDiscover) {
        this.iconCardDiscover = iconCardDiscover;
        this.titleCardDiscover = titleCardDiscover;
    }

    public int getIconCardDiscover() {
        return iconCardDiscover;
    }

    public String getTitleCardDiscover() {
        return titleCardDiscover;
    }
}
